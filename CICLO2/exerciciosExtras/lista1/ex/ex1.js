/* Faça um programa que receba quatro
números inteiros, calcule e mostre a soma desses números. */

N1 = parseInt(prompt("Insira o primeiro número:"));
console.log("O primeiro número informado foi: " + N1);

N2 = parseInt(prompt("Insira o segundo número:"));
console.log("O primeiro número informado foi: " + N2);

N3 = parseInt(prompt("Insira o terceiro número:"));
console.log("O primeiro número informado foi: " + N3);

N4 = parseInt(prompt("Insira o quarto número:"));
console.log("O primeiro número informado foi: " + N4);

soma = N1 + N2 + N3 + N4;
console.log("A soma dos números informados é: " + soma);
document.write("A soma dos números informados é: " + soma);